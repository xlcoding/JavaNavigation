<h3 id="简历的重要性" tabindex="-1"><a class="header-anchor" href="#简历的重要性" aria-hidden="true">#</a> 简历的重要性</h3>
<p>即使你有很牛逼的技术，但是你初筛过不了，连面试机会都没有，你说个der.....。</p>
<p><strong>初筛环节</strong>：在招聘高峰期，HR 一天就要看好几百份乃至更多简历，每份简历不可能花太多时间，所以你得学会如何在短短几十秒去吸引抓住 HR 眼球，符合招聘要求并且还觉得你很牛逼。</p>
<p><strong>面试环节</strong>：面试中，面试官一般是根据你的简历提问，而你把简历写好，可以很好的引导面试官去提问，趋利避害。比如，你在简历上把你擅长的部分突出出来，“熟悉掌握MYSQL，MYSQL架构、<strong>索引原理</strong>、<strong>事务原理与锁机制</strong>、日志机制等，有一定 SQL 优化经验”，那么面试官刚开始拿到简历，可能还没想到问什么，第一眼便会看到你的 “<code v-pre>索引、事务、锁</code>” 相关的，然后便很大概率去问你这方面的内容。</p>
<p>简历的内容在精不在多，尽量保持在一页即可，少用口水话，直接抓住核心+亮点+你擅长的。</p>
<p>不用搞花里胡哨，讲太多其实没用。</p>
