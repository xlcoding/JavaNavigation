<h3 id="具体细节" tabindex="-1"><a class="header-anchor" href="#具体细节" aria-hidden="true">#</a> 具体细节</h3>
<h4 id="专业技能" tabindex="-1"><a class="header-anchor" href="#专业技能" aria-hidden="true">#</a> 专业技能</h4>
<p>把握好专业技能的描述，专业技能的熟悉一般分为三个层次：</p>
<ul>
<li>精通：最好不要写精通，会被面试官问到怀疑人生....。</li>
<li>熟悉/熟练/掌握：一般都写熟悉XX的使用，对于经常使用到的技术，主要用的技术栈，一般都这样写。比如使用SSM、MySQL等。</li>
<li>了解：知道这玩意，或则自己只会一点，或则准备不充分，害怕被面试官问穿的。比如 “分布式”。</li>
</ul>
<blockquote>
<p>注意：简历上提到的技术栈，一定要是你能回答上来的，即使你不会，但是问你问题你要能答（八股~），建议写自己会的，然后再背八股比较好（点击—<a href="https://mp.weixin.qq.com/s?__biz=MzkxMjE5NzUxNQ==&amp;mid=2247484408&amp;idx=1&amp;sn=7187ec64a0c77fa79cdf364ae60a7596&amp;chksm=c111d11cf666580a3ce70684b103e14e667e54c2c7de9845ac0fee807a2159e284379b1dc61f&amp;token=1311259124&amp;lang=zh_CN#rd" target="_blank" rel="noopener noreferrer">面试笔记<ExternalLinkIcon/></a>）</p>
</blockquote>
<p>技术点尽量写详细一点，如果要写MySQL相关，不要只写熟悉 MySQL的原理和基本使用。可以换成 “熟悉掌握MYSQL，MYSQL架构、<strong>索引原理</strong>、<strong>事务原理与锁机制</strong>、日志机制等，有一定SQL优化经验”，然后把你最擅长最牛逼的点用 <code v-pre>黑色加粗凸</code> 显出来，不要只写泛泛概念，一定 <code v-pre>落实到具体哪个点</code>。</p>
<p><strong>为什么这样做？</strong></p>
<p>对面试官来讲，面试官刚拿到你的简历，他可能没有想好具体的问题。</p>
<p>我们把专业技能写的很细，落实到具体某个点，面试官有很大可能去问这些专业技能了，然后这又是你熟悉的领域，如果回答的很漂亮，将是很大的加分项。</p>
<p>并且面试是有时间限制的，这方面聊的多了，那么其他方面就聊的少了，遇到不熟悉的概率就更小了。</p>
<p><strong>比如：</strong></p>
<ul>
<li>具有扎实Java基础，熟练掌握集合、多线程、反射等基础框架，对<strong>集合</strong>和<strong>多线程</strong>有深入研究。</li>
<li>熟悉常用的<strong>数据结构与算法</strong>、<strong>设计模式</strong>。</li>
<li>掌握计算机网络，掌握TCP，UDP，HTTP等协议。</li>
<li>掌握<strong>进程通信</strong>、<strong>内存管理、虚拟内存</strong>等操作系统知识。</li>
<li>熟系掌握MYSQL，<strong>MYSQL架构、索引原理、事务原理与锁机制、日志机制等，有一定SQL优化经验</strong>熟悉</li>
<li>掌握Redis核心<strong>底层数据结构</strong>，主从复制，持久化、<strong>Redis分布式锁等。</strong></li>
<li>熟悉JVM虚拟机基本原理，包括<strong>内存模型</strong>，<strong>垃圾回收机制</strong>，<strong>类加载机制等</strong>。</li>
<li>掌握SpringMVC、Spring、Mybatis、Springboot、Vue.js等主流框架使用。熟悉前后端分离开发，有 SpringCloud项目经验。</li>
<li>了解Element UI、Vant UI等组件库。</li>
</ul>
