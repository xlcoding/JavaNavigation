<h4 id="前端-了解" tabindex="-1"><a class="header-anchor" href="#前端-了解" aria-hidden="true">#</a> 前端（了解）</h4>
<p>对于前端这块，我觉得不管你是前端、后端，Html、CSS、JS的基础知识是每个人必备的</p>
<p>其次，作为后端，可以学习一下 Vue.js，这个框架，你需要理解<code v-pre>前后端分离开发</code>，需要理解<code v-pre>前后端请求对接</code></p>
<p><strong>为什么呢？</strong></p>
<ul>
<li>一方面：你可以独自做个简单前端完成一个完整项目开发，平时学习作业等</li>
<li>二方面：即使面试可能不问，但是你进入稍微大一点的公司都是前后端分离开发，这时作为后端的你务必会与前端进行接口对接，若你不会，那么效率会特别底下，浪费很多时间的。</li>
</ul>
<p>上述我认为是必须要掌握的，然后若你对小程序开发感兴趣，可以学习 uniapp。</p>
